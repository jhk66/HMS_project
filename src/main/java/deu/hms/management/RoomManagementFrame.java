    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JPanel inputPanel = new JPanel();
        inputPanel.setLayout(new BoxLayout(inputPanel, BoxLayout.Y_AXIS));

        JLabel floorLabel = new JLabel("층수");
        floorLabel.setFont(new Font("맑은 고딕", Font.BOLD, 16));
        floorLabel.setAlignmentX(Component.CENTER_ALIGNMENT);
        inputPanel.add(floorLabel);

        JTextField floorLabelField = new JTextField();
        floorLabelField.setPreferredSize(new Dimension(300, 50));
        inputPanel.add(floorLabelField);

        JLabel ratingLabel = new JLabel("등급");
        ratingLabel.setFont(new Font("맑은 고딕", Font.BOLD, 16));
        ratingLabel.setAlignmentX(Component.CENTER_ALIGNMENT);
        inputPanel.add(ratingLabel);

        JTextField ratingLabelField = new JTextField();
        ratingLabelField.setPreferredSize(new Dimension(300, 50));
        inputPanel.add(ratingLabelField);

        JLabel priceLabel = new JLabel("가격");
        priceLabel.setFont(new Font("맑은 고딕", Font.BOLD, 16));
        priceLabel.setAlignmentX(Component.CENTER_ALIGNMENT);
        inputPanel.add(priceLabel);

        JTextField priceField = new JTextField();
        priceField.setPreferredSize(new Dimension(300, 50));
        inputPanel.add(priceField);

        boolean validInput = false;

        while (!validInput) {
            int result = JOptionPane.showConfirmDialog(this, inputPanel, "객실 정보 등록", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (result == JOptionPane.OK_OPTION) {
                String floor = floorLabelField.getText().trim();
                String rate = ratingLabelField.getText().trim();
                String priceString = priceField.getText().trim();

                if (floor.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "층수를 입력하세요.", "Error", JOptionPane.ERROR_MESSAGE);
                    continue;
                }
                if (rate.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "등급을 입력하세요.", "Error", JOptionPane.ERROR_MESSAGE);
                    continue;
                }
                if (priceString.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "가격을 입력하세요.", "Error", JOptionPane.ERROR_MESSAGE);
                    continue;
                }
                try {
                    int price = Integer.parseInt(priceString);
                    DefaultTableModel model = (DefaultTableModel) roomInfoTable.getModel();
                    model.addRow(new Object[]{floor, rate, price});
                    validInput = true;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "올바른 가격을 입력하세요.", "Error", JOptionPane.ERROR_MESSAGE);
                    continue;
                }
            } else {
                break;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void loadRoomInfo() {
        DefaultTableModel roominfoModel = (DefaultTableModel) roomInfoTable.getModel();
        new RoomInfoLoad(roominfoModel);
    }

