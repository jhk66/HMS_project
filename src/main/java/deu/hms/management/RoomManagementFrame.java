    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if (roomInfoTable.getCellEditor() != null) {
            roomInfoTable.getCellEditor().stopCellEditing();
        }
        
        JPanel inputPanel = new JPanel();
        inputPanel.setLayout(new BoxLayout(inputPanel, BoxLayout.Y_AXIS));
        
        JLabel reasonLabel = new JLabel("수정사항 및 이유를 입력하시오.");
        reasonLabel.setFont(new Font("맑은 고딕", Font.BOLD, 14));
        reasonLabel.setAlignmentX(Component.CENTER_ALIGNMENT);
        inputPanel.add(reasonLabel);
        
        JTextField reasonField = new JTextField();
        reasonField.setPreferredSize(new Dimension(300, 50));
        inputPanel.add(reasonField );
        
        boolean validInput = false;

        while (!validInput) {
            int result = JOptionPane.showConfirmDialog(this, inputPanel, "수정 및 저장", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (result == JOptionPane.OK_OPTION) {
                String reason = reasonField.getText().trim();
                if (reason.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "내용 입력하세요.", "Error", JOptionPane.ERROR_MESSAGE);
                    continue;
                }
                try {
                    new ChangeSaveFile(roomInfoTable, roominfoFile);
                    JOptionPane.showMessageDialog(null, "변경사항이 저장되었습니다." );
                    validInput = true;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "내용을 입력하세요.", "Error", JOptionPane.ERROR_MESSAGE);
                    continue;
                }
            }
            else{
                break;
            }
        }
        
        
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int selectedRow = roomInfoTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "삭제할 행을 선택하세요.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) roomInfoTable.getModel();
        model.removeRow(selectedRow);
    }//GEN-LAST:event_deleteButtonActionPerformed

